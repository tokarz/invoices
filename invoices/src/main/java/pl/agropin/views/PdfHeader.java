package pl.agropin.views;

public class PdfHeader {
	private String nazwaFirmy;
	private String nip;
	private String okresRozliczeniowy;
	private String pesel;
	private String adres;
	private String stawkaPlaca;
	private String dzialSekcjaPracownika;
	private String imie;
	private String nazwisko;
	public String getNazwaFirmy() {
		return nazwaFirmy;
	}
	public void setNazwaFirmy(String nazwaFirmy) {
		this.nazwaFirmy = nazwaFirmy;
	}
	public String getNip() {
		return nip;
	}
	public void setNip(String nip) {
		this.nip = nip;
	}
	public String getOkresRozliczeniowy() {
		return okresRozliczeniowy;
	}
	public void setOkresRozliczeniowy(String okresRozliczeniowy) {
		this.okresRozliczeniowy = okresRozliczeniowy;
	}
	public String getPesel() {
		return pesel;
	}
	public void setPesel(String pesel) {
		this.pesel = pesel;
	}
	public String getAdres() {
		return adres;
	}
	public void setAdres(String adres) {
		this.adres = adres;
	}
	public String getStawkaPlaca() {
		return stawkaPlaca;
	}
	public void setStawkaPlaca(String stawkaPlaca) {
		this.stawkaPlaca = stawkaPlaca;
	}
	public String getDzialSekcjaPracownika() {
		return dzialSekcjaPracownika;
	}
	public void setDzialSekcjaPracownika(String dzialSekcjaPracownika) {
		this.dzialSekcjaPracownika = dzialSekcjaPracownika;
	}
	public String getImie() {
		return imie;
	}
	public void setImie(String imie) {
		this.imie = imie;
	}
	public String getNazwisko() {
		return nazwisko;
	}
	public void setNazwisko(String nazwisko) {
		this.nazwisko = nazwisko;
	}
}
