package pl.agropin.views;

public class Part {
	private String symbolSkladnika;
	private String nazwaSkladnika;
	private String wskaznikOdc;
	private String ileGodzin;
	private String procent;
	private String kwota;
	public String getSymbolSkladnika() {
		return symbolSkladnika;
	}
	public void setSymbolSkladnika(String symbolSkladnika) {
		this.symbolSkladnika = symbolSkladnika;
	}
	public String getNazwaSkladnika() {
		return nazwaSkladnika;
	}
	public void setNazwaSkladnika(String nazwaSkladnika) {
		this.nazwaSkladnika = nazwaSkladnika;
	}
	public String getWskaznikOdc() {
		return wskaznikOdc;
	}
	public void setWskaznikOdc(String wskaznikOdc) {
		this.wskaznikOdc = wskaznikOdc;
	}
	public String getIleGodzin() {
		return ileGodzin;
	}
	public void setIleGodzin(String ileGodzin) {
		this.ileGodzin = ileGodzin;
	}
	public String getProcent() {
		return procent;
	}
	public void setProcent(String procent) {
		this.procent = procent;
	}
	public String getKwota() {
		return kwota;
	}
	public void setKwota(String kwota) {
		this.kwota = kwota;
	}
}
